#include "libft.h"

int     ft_isstrnum(char *str)
{
    if (!str || !*str)
        return (1);
    if (*str == '+' || *str == '-')
        str++;
    while (*str)
    {
        if (!ft_isdigit(*str++))
            return (0);
    }
    return (1);
}
